        <h3>St micheal's cathedral</h3>
        <p>The St Michael's Cathedral is the mother Church of the diocese. In the years leading up to the creation of Awgu diocese out of Enugu diocese, the Bishop of Enugu at the time, Most Rev Anthony O Gbuji started the construction of a Church to house the new and young diocese of Awgu. This Church is today the St Michael's Cathedral which hosts most of the diocesan functions.It has however been observed that there are many leakages on the roof of the Church. Each time it rains, several parts of the Cathedral are filled with rain droppings. The situation is more worrisome, in situations of rain fall during liturgical functions.

        An appeal has been launched to raise funds for the reconstruction and renovation of the Cathedral. The Bishop has approved that prayers be said to this intention at all masses celebrated in the diocese.

        An estimate submitted by the diocesan building Engineer, show that the total cost of N……… shall be spent for an overall reconstruction and renovation of the Cathedral. A passionate appeal is hereby made to all people of good will to support the work of evangelization in the diocese of Awgu through prayers and financial donations towards a befitting place of worship for God to His greater glory and honour.


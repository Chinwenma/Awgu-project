        <h2>THE BULL OF ERECTION OF AWGU DIOCESE</h2>
        <h3>Benedict Bishop servant of the servants of God for an everlasting remembrance</h3>
        <li>
        <p>The Evangelical Solicitude, which urges us in the daily discharge of our very important duty, now impels us to pay a special attention to the Church in Nigeria. It is with heart full of joy that we consider the spiritual progress of the faithful of that country and thought of establishing there a new diocese. Accordingly, after hearing the requests of those concerned and with the advice of the Congregation for the Evangelization of Peoples, in the fullness of our apostolic power, we carve out from the diocese of Enugu the territory comprising the following Local Government Areas: Awgu, Agbogugu, Ndeabor, Nnenwe, Oji Rivers, (except for the parish called Ukwuuoba), and Owelli. We also establish the territories of the district a new diocese to be called Awgu diocese. We established the seat of the new ecclesial community in Awgu city. Similarly we raise the church building there dedicated to God in honour of Saint Michael the Archangel to the status of a Cathedral Church with all the customary right and privileges.</p>
    </li>
        <li>
        <p>We establish Awgu diocese as a suffragan of the Metropolitan Sea of Onitsha and place it under the jurisdiction of the Congregation for the Evangelization of Peoples. Other things are governed by the norms of canon law. In order to carry out all we have stated in this decree, we deputise our venerable brother, Renzo Fratini the Archbishop of Botriana and Apostolic Delegate in Nigeria giving him all the necessary faculties and also of sub delegating any man endowed with ecclesiastical dignity to carry out this assignment with the obligation to send back to the Congregation for the Evangelization of Peoples an authentic copy of the acts of the assignment accomplished.Finally, we want this our decree to be considered ratified for now and the future, everything else to the contrary not withstanding. Given in Rome at St. Peter's on the day of July, 2005, the first year of our Pontificate.</p>
        </li>

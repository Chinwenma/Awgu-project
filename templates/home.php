        <h5>Short Biography of Rt. Rev. Msgr. John Ifeanyichukwu Okoye</h5>
        <p>Fifty-five years lie between the years 1950 and 2005; one the birth of Rev. Msgr. John Ifeanyichuwu Okoye and the other his call to the episcopacy! When he was born in his little homestead of Ezinato Quarters of Umuhu Village, Owelli-Court in the present day Mbanabo Local Government Council of Enugu State, his parents Mr. Paul and Mrs. Janet Okoye were happy like every other married couples blessed with children by God.
          They nursed him, nurtures him and cared for him. Even as they looked at the nimble feet of the little Ifeanyi and his tiny eyes they wished him well from all their hearts as parents usually do. But they did not know –they couldn't know – that they were carrying in their hands the would-be first bishop of Awgu Catholic Diocese. Every acron could become an oak but certainly not all; in fact it is even an exception rather than the rule. Every male could become a priest, just like every priest become a bishop, but only very few males become priests and still much fewer become bishops! Many are called but few are chosen!</p>
